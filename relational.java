public class relational {
    public static void main(String[] args) {
        float a=3.6f;
        float b=7.2f;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a>=b);
    }
}
